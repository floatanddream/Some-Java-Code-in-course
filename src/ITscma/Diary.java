package ITscma;

public class Diary {

        String time;    //时间
        String name; //姓名
        String weather;//天气
        String title;//标题
        String content;//内容
        public Diary(String time, String name, String weather, String title, String content) {
            this.time = time;
            this.name = name;
            this.weather = weather;
            this.title = title;
            this.content = content;
        }

}
